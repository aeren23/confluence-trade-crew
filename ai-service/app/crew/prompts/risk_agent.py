    "Step 4: Calculate position size: risk_amount = {balance} * ({risk_percentage} / 100).\n"
    "Step 5: Determine Stop-Loss based on direction.\n"
    "   FIRST: Extract support and resistance levels from the TA Agent's INDICATOR_DATA JSON block "
