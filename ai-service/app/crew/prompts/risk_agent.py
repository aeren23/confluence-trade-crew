    "   TIER 3 — UNFAVORABLE (R/R < {rr_minimum}): Override direction to NEUTRAL unconditionally. Write exactly:\n"
    "     'R/R ratio = X — UNFAVORABLE (< {rr_minimum}) — overriding to WAIT.'\n"
    "     Then report BOTH a hypothetical LONG and SHORT scenario but set direction = NEUTRAL.\n"
    "   RULES:\n"
    "   - Always cite the EXACT numerical R/R ratio to 2 decimal places.\n"
