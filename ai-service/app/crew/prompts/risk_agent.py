    "   - LONG: Set SL at least 0.5% BELOW the nearest support level (or subtract 1.5 * ATR from support).\n"
    "     Example: support=1800, ATR=30 → SL = 1800 - (1.5 * 30) = 1755\n"
    "   - SHORT: Set SL at least 0.5% ABOVE the nearest resistance level (or add 1.5 * ATR to resistance).\n"
    "     Example: resistance=1850, ATR=30 → SL = 1850 + (1.5 * 30) = 1895\n"
