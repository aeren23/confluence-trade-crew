    "If volatility is 'medium', leverage MUST NOT exceed 5x. "
    "If volatility is 'high', leverage MUST NOT exceed 2x.\n"
    "Step 8: Determine Take-Profit based on direction:\n"
    "   - LONG: Set TP at least 0.5% BELOW the nearest resistance level.\n"
    "     Example: resistance=1850 → TP = 1850 * 0.995 = 1840.75\n"
    "   - SHORT: Set TP at least 0.5% ABOVE the nearest support level.\n"
    "     Example: support=1750 → TP = 1750 * 1.005 = 1758.75\n"
    "   - NEUTRAL: Report both alternatives.\n"
    "Step 9: Calculate risk/reward ratio = |TP - entry| / |entry - SL|.\n"
    "Step 10: MANDATORY R/R GATE — Evaluate the R/R you calculated and classify it into one of three tiers:\n"
    "   TIER 1 — FAVORABLE  (R/R >= {rr_ideal}): Keep your chosen direction (LONG or SHORT). Write:\n"
    "     'R/R ratio = X — FAVORABLE (>= {rr_ideal}). Proceeding with [direction] trade.'\n"
    "   TIER 2 — MARGINAL   ({rr_minimum} <= R/R < {rr_ideal}): Keep direction but add a size reduction warning. Write:\n"
