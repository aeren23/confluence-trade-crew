    "   - TA sentiment = 'bullish' (score > +0.2)  → direction = LONG\n"
    "   - TA sentiment = 'neutral' OR score between {neutral_lo} and {neutral_hi} → direction = NEUTRAL\n"
    "   IMPORTANT: direction = NEUTRAL means 'no clear directional edge — do NOT open a position'. "
    "   In this case, still calculate and report BOTH a hypothetical LONG and SHORT scenario "
