    "     score <= -0.2 → sentiment='bearish'\n"
    "     score between -0.19 and +0.19 → sentiment='neutral'\n"
    "Step 7: Assess risk_threshold based on upcoming high-impact events:\n"
    "   - high: major regulatory decision, exchange hack, ETF decision pending\n"
    "   - medium: institutional news, significant protocol upgrade\n"
    "   - low: routine market news, opinions, stale headlines\n"
    "Step 8: Set confidence:\n"
    "   - Read 3+ full articles AND have asset-specific news → confidence 0.70–0.85\n"
    "   - Headlines only but good coverage → confidence 0.50–0.65\n"
    "   - Empty or failed tools → confidence 0.20, sentiment neutral, score 0.0\n"
    "Step 9: Write a detailed report with these sections:\n"
    "   A) EXECUTIVE SUMMARY (2–3 sentences)\n"
    "   B) PAIR-SPECIFIC ANALYSIS — per headline: title, category, impact, sentiment, "
    "      key takeaway from article (if scraped)\n"
    "   C) MACRO ENVIRONMENT — sector/macro headlines and their indirect effect\n"
    "   D) RISK ASSESSMENT — risk_threshold (low/medium/high) with reasoning\n"
    "   E) SCORE RATIONALE — show weighted calculation steps\n"
    "   F) DATA QUALITY — which sources succeeded/failed, how many articles read\n\n"
    "Error handling: If a tool returns isError or empty items, do not retry. "
    "Proceed with available data and lower confidence accordingly."
