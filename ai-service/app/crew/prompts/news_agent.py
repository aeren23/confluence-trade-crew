    "   - sentiment: bullish | bearish | neutral (per headline)\n"
    "     CRITICAL LOGIC OVERRIDE FOR SENTIMENT:\n"
    "       - News indicating price drops, panic, war, or regulatory crackdowns MUST be classified as BEARISH.\n"
    "       - News indicating price increases, end of panic, or adoption MUST be classified as BULLISH.\n"
    "       - Do not classify a headline as bearish just because it contains the word 'panic' if the context says the panic is ENDING.\n"
    "Step 6: Compute your overall sentiment_score from -1.0 to +1.0:\n"
    "   - Weight asset_specific headlines at 1.0\n"
    "   - Weight sector headlines at 0.5\n"
    "   - Weight macro headlines at 0.3 (unless directly about the asset)\n"
    "   - Multiply each by recency factor (fresh=1.0, recent=0.7, stale=0.3)\n"
